interface LanguageProviderProps {
  children: ReactNode
}

export function LanguageProvider({ children }: LanguageProviderProps) {
  // Neerland√©s como idioma principal por defecto
  const [language, setLanguageState] = useState<Language>('nl')

  useEffect(() => {
    const savedLanguage = localStorage.getItem('lissewege-language') as Language
    if (savedLanguage && ['nl', 'en', 'es'].includes(savedLanguage)) {
      setLanguageState(savedLanguage)
    } else {
      // Si no hay idioma guardado, usar neerland√©s por defecto
      setLanguageState('nl')
      localStorage.setItem('lissewege-language', 'nl')
    }
  }, [])

  const setLanguage = (lang: Language) => {
    setLanguageState(lang)
    localStorage.setItem('lissewege-language', lang)
  }

  const t = (key: string): string => {
    return (translations[language] as any)[key] || key
  }

  return (
    <LanguageContext.Provider value={{ language, setLanguage, t }}>
      {children}
    </LanguageContext.Provider>
  )
}

export function useLanguage() {
  const context = useContext(LanguageContext)
  if (context === undefined) {
    throw new Error('useLanguage must be used within a LanguageProvider')
  }
  return context
}